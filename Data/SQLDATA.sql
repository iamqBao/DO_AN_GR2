CREATE DATABASE QUAN_LY_TIEM_NET
GO

USE QUAN_LY_TIEM_NET
GO

CREATE TABLE STAFF
(
    ID NVARCHAR(5) NOT NULL,
	FULLNAME NVARCHAR(20),
	TELL VARCHAR(12),
	SEX BIT,
    CCCD NVARCHAR(12),
	BIRTHDAY DATE,
	PERMIT NVARCHAR(12),
	SALARY INT
	PRIMARY KEY(ID)
)
GO

CREATE TABLE CUSMTOMER
(
    ID NVARCHAR(5) NOT NULL,
	FULLNAME NVARCHAR(20),
	TELL VARCHAR(12),
	SEX BIT,
    CCCD NVARCHAR(12),
	BIRTHDAY DATE,
	USERNAME NVARCHAR(30) NOT NULL,
	RANKING NVARCHAR(10),
	PRIMARY KEY(ID)
)
GO

CREATE TABLE PC
(
    ID NVARCHAR(5) NOT NULL,
	NAMEPC NVARCHAR(20),
	TYPEPC NVARCHAR(12),
	OS NVARCHAR(10),
	HIREPRICE INT
	PRIMARY KEY(ID)
)
GO

CREATE TABLE BREAKFAST
(
    ID NVARCHAR(5) NOT NULL,
	FULLNAME NVARCHAR(40),
	BRAND VARCHAR(30),
	PRICE INT,
	SIZE NVARCHAR(3),
    QUANTITY INT,
	PRIMARY KEY(ID)
)
GO